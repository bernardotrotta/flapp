DROP DATABASE IF EXISTS flapp;
CREATE DATABASE flapp;
USE flapp;

CREATE TABLE Passeggeri (
ID_PASSEGGERO VARCHAR(10) PRIMARY KEY,
Nome VARCHAR(100) NOT NULL,
Cognome VARCHAR(100) NOT NULL,
Codice_fiscale VARCHAR(16) UNIQUE NOT NULL,
Data_nascita DATE,
Telefono VARCHAR(18) NOT NULL,
Indirizzo_mail VARCHAR(100) NOT NULL
);

CREATE TABLE Aeroporti (
ID_AEROPORTO VARCHAR(10) PRIMARY KEY,
Paese VARCHAR(100),
Città VARCHAR(100),
Codice_iata CHAR(3) UNIQUE,
Nome VARCHAR(100)
);

CREATE TABLE Aerei (
ID_AEREO VARCHAR(6) PRIMARY KEY,
Modello VARCHAR(100),
Capacità INT,
Immagine VARCHAR(2048)
);

CREATE TABLE Personale (
ID_DIPENDENTE VARCHAR(10) PRIMARY KEY,
Nome VARCHAR(100) NOT NULL,
Cognome VARCHAR(100) NOT NULL,
Data_nascita DATE,
Codice_fiscale VARCHAR(16) UNIQUE NOT NULL,
Ruolo VARCHAR(100) NOT NULL,
Password VARCHAR(255) NOT NULL
);

CREATE TABLE Voli (
ID_VOLO VARCHAR(10) PRIMARY KEY,
Data_partenza DATE,
Data_arrivo DATE,
Orario_partenza TIME,
Orario_arrivo TIME,
Durata TIME,
Aeroporto_partenza VARCHAR(10),
Aeroporto_arrivo VARCHAR(10),
Aereo VARCHAR(6),
FOREIGN KEY (Aeroporto_partenza) REFERENCES Aeroporti(ID_AEROPORTO),
FOREIGN KEY (Aeroporto_arrivo) REFERENCES Aeroporti(ID_AEROPORTO),
FOREIGN KEY (Aereo) REFERENCES Aerei(ID_AEREO)
);

CREATE TABLE Prenotazioni (
ID_PRENOTAZIONE VARCHAR(10) PRIMARY KEY,
Passeggero VARCHAR(10),
Volo VARCHAR(10),
Prezzo_biglietto DECIMAL(10, 2),
FOREIGN KEY (Passeggero) REFERENCES Passeggeri(ID_PASSEGGERO),
FOREIGN KEY (Volo) REFERENCES Voli(ID_VOLO)
);

CREATE TABLE Servizio (
ID_dipendente VARCHAR(10) ,
ID_volo VARCHAR(10) ,
PRIMARY KEY (ID_dipendente, ID_volo),
FOREIGN KEY (ID_dipendente) REFERENCES Personale(ID_DIPENDENTE),
FOREIGN KEY (ID_volo) REFERENCES Voli(ID_VOLO)
);

CREATE TABLE Recensioni (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nome_utente VARCHAR(50) NOT NULL,
    Contenuto TEXT NOT NULL,
    Valutazione TINYINT CHECK (valutazione BETWEEN 1 AND 5),
    Data_recensione TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

